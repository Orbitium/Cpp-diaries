Map::Map() {
	grassTexture = TextureManager::loadTexture("resources/grass.bmp");
	dirtTexture = TextureManager::loadTexture("resources/dirt.bmp");

	loadMap(level1);
}

void Map::loadMap(int arr[20][25]) {

	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++) {
			map[row][column] = arr[row][column];
		}
	}

	src.x = src.y = 0;
	src.w = dest.w = 64;
	src.h = dest.h = 64;

	dest.x = dest.y = 0;
}


void Map::drawMap() {

	int type = 0;
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++) {
			type = map[row][column];
			
			dest.x = column * 32;
			dest.y = row * 32;

			switch (type) {
			case 1:
				TextureManager::draw(dirtTexture, src, dest);
				break;
			case 2:
				TextureManager::draw(grassTexture, src, dest);
				break;
			}
		}
	}
}